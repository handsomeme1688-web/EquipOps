-- 临时写一个 JUnit 方法执行一次，取出输出：
--
--     var encoder = new BCryptPasswordEncoder();
--     System.out.println(encoder.encode("你的初始密码"));
--
-- 输出形如 $2a$10$... 的 60 字符串，替换本文件中全部
-- __REPLACE_WITH_BCRYPT_HASH__ 占位符即可（8 个账号可共用同一个哈希）。
--
-- ⚠️ 注意事项：
--   1. 本仓库是公开的。不要使用 123456、admin 这类弱密码，
--      也不要把生产环境密码写进来。
--   2. 初始密码写入 README 的「本地启动」章节，并注明首次登录后应修改。
--   3. BCrypt 每次调用产生的哈希不同（盐是随机的），但都能校验通过同一明文，
